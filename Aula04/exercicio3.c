int main(int argc, char const *argv[])
{

//adidiconando +1 até chegar em 10 usando "for"

    int x;

    for (x = 0; x < 11; x++)
    {
        printf("%d\n", x);
    }


//adicionando +1 até chegar usando "while" (mais fácil)

    int y = 0;
    while (y < 11)
    {
        printf("%d\n", y++);

    }
    
    



    return 0;
}
